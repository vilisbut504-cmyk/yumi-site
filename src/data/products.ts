export type ProductHardness = 'мягкое' | 'среднее' | 'плотное' | 'жевательное'
export type ProductUnit = 'weight' | 'piece'
export type ProductStatus = 'active' | 'draft' | 'no_price'

export type Product = {
  id: string
  slug: string
  name: string
  category: string
  protein: string
  purposes: string[]
  dogSizes: string[]
  texture: string
  hardness: ProductHardness
  format: string
  unit: ProductUnit
  weight: string
  price: number
  price100g: number | null
  oldPrice?: number | null
  status: ProductStatus
  imagePaths: string[]
  shortDescription: string
  composition: string
  howToUse: string
  storage: string
  warning: string
  tags: string[]
  isFeatured: boolean
}

const NOT_A_MEAL = 'Не является заменой основного рациона.'
const STORAGE_DEFAULT =
  'Хранить в сухом прохладном месте, в закрытой упаковке, вдали от прямых солнечных лучей.'
const USE_DEFAULT =
  'Давайте как дополнение к основному рациону. Вводите постепенно и следите за реакцией собаки. Обеспечьте доступ к свежей воде.'

const ALL_SIZES = ['Малые', 'Средние', 'Крупные']

type ActiveInput = {
  id: string
  slug: string
  name: string
  category: string
  protein: string
  purposes: string[]
  dogSizes?: string[]
  texture: string
  hardness: ProductHardness
  format?: string
  unit?: ProductUnit
  price: number
  oldPrice?: number | null
  imagePaths: string[]
  shortDescription: string
  composition: string
  tags: string[]
  isFeatured?: boolean
}

function active(p: ActiveInput): Product {
  const unit = p.unit ?? 'weight'
  return {
    id: p.id,
    slug: p.slug,
    name: p.name,
    category: p.category,
    protein: p.protein,
    purposes: p.purposes,
    dogSizes: p.dogSizes ?? ALL_SIZES,
    texture: p.texture,
    hardness: p.hardness,
    format: p.format ?? '',
    unit,
    weight: unit === 'piece' ? '1 шт' : '100 г',
    price: p.price,
    price100g: unit === 'weight' ? p.price : null,
    oldPrice: p.oldPrice ?? null,
    status: 'active',
    imagePaths: p.imagePaths,
    shortDescription: p.shortDescription,
    composition: p.composition,
    howToUse: USE_DEFAULT,
    storage: STORAGE_DEFAULT,
    warning: NOT_A_MEAL,
    tags: p.tags,
    isFeatured: p.isFeatured ?? false,
  }
}

type DraftInput = {
  id: string
  slug: string
  name: string
  category: string
  protein: string
  purposes: string[]
  dogSizes?: string[]
  texture: string
  hardness: ProductHardness
  format?: string
  unit?: ProductUnit
  imagePaths: string[]
  shortDescription: string
  composition: string
  tags: string[]
}

function draft(p: DraftInput): Product {
  const unit = p.unit ?? 'weight'
  return {
    id: p.id,
    slug: p.slug,
    name: p.name,
    category: p.category,
    protein: p.protein,
    purposes: p.purposes,
    dogSizes: p.dogSizes ?? ALL_SIZES,
    texture: p.texture,
    hardness: p.hardness,
    format: p.format ?? '',
    unit,
    weight: unit === 'piece' ? '1 шт' : '100 г',
    price: 0,
    price100g: null,
    oldPrice: null,
    status: 'no_price',
    imagePaths: p.imagePaths,
    shortDescription: p.shortDescription,
    composition: p.composition,
    howToUse: USE_DEFAULT,
    storage: STORAGE_DEFAULT,
    warning: NOT_A_MEAL,
    tags: p.tags,
    isFeatured: false,
  }
}

